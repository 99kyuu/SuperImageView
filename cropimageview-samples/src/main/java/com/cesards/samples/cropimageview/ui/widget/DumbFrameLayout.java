package com.cesards.samples.cropimageview.ui.widget;

import android.annotation.TargetApi;
import android.content.Context;
import android.os.Build;
import android.util.AttributeSet;
import android.widget.FrameLayout;

public class DumbFrameLayout extends FrameLayout {

  private CroppedAreaImageView croppedAreaImageView;

  public DumbFrameLayout(Context context) {
    super(context);
  }

  public DumbFrameLayout(Context context, AttributeSet attrs) {
    super(context, attrs);
  }

  public DumbFrameLayout(Context context, AttributeSet attrs, int defStyleAttr) {
    super(context, attrs, defStyleAttr);
  }

  @TargetApi(Build.VERSION_CODES.LOLLIPOP)
  public DumbFrameLayout(Context context, AttributeSet attrs, int defStyleAttr, int defStyleRes) {
    super(context, attrs, defStyleAttr, defStyleRes);
  }

  @Override
  protected void onFinishInflate() {
    super.onFinishInflate();
    croppedAreaImageView = (CroppedAreaImageView) getChildAt(0);
  }

  @Override
  protected void onLayout(boolean changed, int left, int top, int right, int bottom) {
    super.onLayout(changed, left, top, right, bottom);
    croppedAreaImageView.setParentBounds(right - left, bottom - top);
  }
}
